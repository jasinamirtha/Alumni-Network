package com.example.sanjeev.alumninetwork.json_model;


public class android_versions {
    private String ver;
    private String name;
    private String api;

    //Getters and setters

    public String getver() {
        return ver;
    }

    public void setver(String ver) {
        this.ver = ver;
    }

    public String getname() {
        return name;
    }

    public void setname(String name) {
        this.name = name;
    }

    public String getapi() {
        return api;
    }

    public void setapi(String api) {
        this.api = api;
    }
}